void user_main(void)
{
    while (TRUE) {
        gPwm[0] = 0x32 | 0x80;
        gPwm[2] = 0x32 | 0x80;
        gPwm[5] = 0x32 | 0x80;
        pwm_out();
        wait_ms(200);
        gPwm[0] = 0x32;
        gPwm[2] = 0x32;
        gPwm[5] = 0x32;
        pwm_out();
        wait_ms(200);
    }
}
